//The final and const keyword are used to declare constants
main() {
  //Final Keyword
  final val1 = 12;
  print(val1);

  //Constant Keyword
  //const keyword is used to represent a compile-time constant
  const pi = 3.14;
  const area = pi * 12 * 12;
  print("The output is ${area}");

  //  final v1 = 12;
  //  const v2 = 13;
  //  v2 = 12;
}
