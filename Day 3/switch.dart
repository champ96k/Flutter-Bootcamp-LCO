main() {
  String grade = "O";
  switch (grade) {
    case "O":
      print("Outstanding Performance");
      break;
    case "A":
      print("Excelent Performance");
      break;
    case "B":
      print("Good Performance");
      break;
    case "F":
      print("Sorry You have Fail");
      break;
    default:
      print("Please Enter Valid Grade");
      break;
  }
}
